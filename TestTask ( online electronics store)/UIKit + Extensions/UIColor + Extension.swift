import UIKit
extension UIColor {
    static func specialOrange() -> UIColor {
        return UIColor(red: 255.0/255.0, green: 110.0/255.0, blue: 78.0/255.0, alpha: 1.0/1.0)
    }
    static func defaultBackgroundColor() -> UIColor {
        return UIColor(red: 0.898, green: 0.898, blue: 0.898, alpha: 1)
    }
}
