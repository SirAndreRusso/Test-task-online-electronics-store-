import UIKit

extension UIFont {
    static func markProBold25() ->UIFont? {
        return UIFont.init(name: "MarkPro-Bold", size: 25)
    }
    static func markPro15() ->UIFont? {
        return UIFont.init(name: "Mark Pro", size: 15)
    }
  
}
